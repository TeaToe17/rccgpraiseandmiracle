// Fuzzy search function for hymns
export function searchHymns(query: string): Hymn[] {
  if (!query || query.trim() === "") {
    return [];
  }

  const searchTerm = query.toLowerCase().trim();
  const searchWords = searchTerm.split(/\s+/);

  // Score each hymn
  const scoredHymns = RCCG_HYMNS.map((hymn) => {
    let score = 0;
    const titleLower = hymn.title.toLowerCase();
    const lyricsLower = hymn.lyrics.toLowerCase();
    const firstLineLower = hymn.firstLine.toLowerCase();
    const authorLower = hymn.author.toLowerCase();

    // Exact title match - highest priority
    if (titleLower === searchTerm) {
      score += 1000;
    } else if (titleLower.includes(searchTerm)) {
      score += 500;
    }

    // Hymn number match
    if (hymn.number === searchTerm) {
      score += 1000;
    }

    // First line match - high priority
    if (firstLineLower.includes(searchTerm)) {
      score += 300;
    }

    // Author match
    if (authorLower.includes(searchTerm)) {
      score += 200;
    }

    // Lyrics match - moderate priority
    if (lyricsLower.includes(searchTerm)) {
      score += 100;
    }

    // Word-by-word matching for multi-word queries
    searchWords.forEach((word) => {
      if (word.length > 2) {
        // Ignore very short words
        if (titleLower.includes(word)) score += 50;
        if (firstLineLower.includes(word)) score += 30;
        if (lyricsLower.includes(word)) score += 10;
        if (authorLower.includes(word)) score += 20;
      }
    });

    return { hymn, score };
  });

  // Filter hymns with score > 0 and sort by score (descending)
  return scoredHymns
    .filter((item) => item.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 10) // Return top 10 results
    .map((item) => item.hymn);
}
